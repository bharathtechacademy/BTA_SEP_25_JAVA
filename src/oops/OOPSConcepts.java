package oops;

public class OOPSConcepts {
	
	
	// class ==> Class is a blueprint or template that defines the properties (attributes) and behaviors (methods) of objects.
	
	//object ==> An object is an instance of a class.
	
	// method ==> A method is block of code or collection of statements written together to complete a specific task.
	
	// constructor ==> A constructor is a special method that is called when an object of a class is created. It initializes the object.
	
	// static ==> The static keyword is used to define class-level members that belong to the class itself rather than to any specific instance of the class.
	
	// inheritance ==> Inheritance is a mechanism that allows a new class (subclass or derived class) to inherit properties and behaviors from an existing class (superclass or base class).
	// extends keyword is used to achieve inheritance in java.
	// types of inheritance ==> single inheritance (class1 extends class2)
	// multilevel inheritance (class2 extends class1 then class3 extends class2)
	// hierarchical inheritance (class2 extends class1 and class3 extends class1)
	
	// encapsulation ==> Encapsulation is the process of bundling data (attributes) and methods (behaviors) together within a class and restricting direct access to the internal state of an object from outside the class.
	// getters and setters are used to achieve encapsulation in java.
	// Encapsulation is a concept of hiding the information.
	// private access modifier is used to achieve encapsulation in java.
	
	// abstraction ==> Abstraction is a concept of hiding the implementation. 	
	// abstract is a keyword used to achieve abstraction in java.
	
	// polymorphism ==> Polymorphism is a concept of having multiple methods with the same name that can perform different tasks. 
	// method overloading (compile-time) and method overriding (run-time ) are used to achieve polymorphism in java.
	// method overloading ==> Having multiple methods in the same class have the same name but different parameters (different type or number of parameters).
	// method overriding ==> Having multiple methods with same name and same parameters between parent class and child class.
	
	
}