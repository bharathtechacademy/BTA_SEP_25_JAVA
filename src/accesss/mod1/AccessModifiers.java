	//4 types of Access Modifiers in Java:
	
	//1. public (Accessible from anywhere-with in the class, package, out side of package) ==> class, method and variable
	//2. protected (Accessible within the same package and child classes in other packages) ==> method and variable
	//3. default (no modifier) (Accessible only within the same package) ==> class, method and variable
	//4. private (Accessible only within the same class)==> method and variable
	
	
